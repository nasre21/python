# listas

my_lista = [1, 2, 3]

# append
my_lista.append(4)
print(my_lista)

# remove

my_lista.remove(2)
print(my_lista)

# extend
my_lista.extend([6, 7, 8])
print(my_lista)

# insert
my_lista.insert(2, 0)

print(my_lista)

# pop
x = my_lista.pop(1)
print(my_lista)
print(x)

# sort

my_lista.sort()
print(my_lista)

# reverse
my_lista.reverse()
print(my_lista)
